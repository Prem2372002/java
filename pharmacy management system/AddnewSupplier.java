    private void okButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_okButtonActionPerformed
        try {                                         
            Connection con = null;
            try {
                con = DriverManager.getConnection("jdbc:mysql://localhost/pharmacy","root","password");
                //JOptionPane.showMessageDialog(null, "Connected");
                
            } catch (SQLException ex) {
                Logger.getLogger(Suppliers.class.getName()).log(Level.SEVERE, null, ex);
                
            }
            
            
            int suppliersid=0;
            PreparedStatement pst = con.prepareStatement("Select max(supplierId) from suppliers");
            ResultSet rs = pst.executeQuery();
            
            while (rs.next()) {
                suppliersid=rs.getInt("max(supplierId)");
            }
            suppliersid++;
            String q = "insert into suppliers values(?,?,?,?,?)";
            pst = con.prepareStatement(q);
            pst.setInt(1, suppliersid);
            pst.setString(2,supName.getText());
            pst.setString(3, suppAddr.getText());
            pst.setDate(4, java.sql.Date.valueOf((dateod.getText())));
            pst.setString(3, emailtf.getText());
            pst.executeUpdate();
            JOptionPane.showMessageDialog(null, "Added New Supplier");
            
            
            
             doClose(RET_OK);
        } catch (SQLException ex) {
            Logger.getLogger(AddNewSupplier.class.getName()).log(Level.SEVERE, null, ex);
            
        }
    }//GEN-LAST:event_okButtonActionPerformed

