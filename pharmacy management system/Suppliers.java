    public Connection getConnection(){
        Connection con = null;
        
        try {
            con = DriverManager.getConnection("jdbc:mysql://localhost/pharmacy","root","password");
            //JOptionPane.showMessageDialog(null, "Connected");
            return con;
        } catch (SQLException ex) {
            Logger.getLogger(Suppliers.class.getName()).log(Level.SEVERE, null, ex);
            return null;
        }
    }
    
    public void fetch(){
        try{
            String q = "select supplierId as `Supplier Id`,suppliername as `Supplier's Name`,supplieraddress as `Supplier's Address`,DateofDelivery as `Date of Last Delivery`,emailid as`Email Id` from Suppliers";
            Connection con = getConnection();
            PreparedStatement pst = con.prepareStatement(q);
            ResultSet rs = pst.executeQuery();
            jTable1.setModel(DbUtils.resultSetToTableModel(rs));
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }
    
    
    
